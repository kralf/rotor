#include "Mutex.h"
#include "MutexImpl.ipp"

using namespace Rotor;

//------------------------------------------------------------------------------

Mutex::Mutex()
  : _mutex( new MutexImpl() )
{
}

//------------------------------------------------------------------------------

Mutex::~Mutex()
{
  delete _mutex;
}

//------------------------------------------------------------------------------

void 
Mutex::lock()
{
  _mutex->lock();
}

//------------------------------------------------------------------------------

void 
Mutex::unlock()
{
  _mutex->unlock();
}
  
